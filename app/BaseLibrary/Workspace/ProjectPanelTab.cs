namespace Celbridge.Workspace;

/// <summary>
/// Defines the available tabs within the ProjectPanel.
/// </summary>
public enum ProjectPanelTab
{
    /// <summary>
    /// No tab selected.
    /// </summary>
    None,

    /// <summary>
    /// The Explorer tab for browsing project files.
    /// </summary>
    Explorer,

    /// <summary>
    /// The Search tab for searching within the project.
    /// </summary>
    Search,

    /// <summary>
    /// The Debug tab for debugging functionality.
    /// </summary>
    Debug,

    /// <summary>
    /// The Source Control tab for source control operations.
    /// </summary>
    SourceControl
}
