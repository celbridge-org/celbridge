namespace Celbridge.Explorer;

/// <summary>
/// Constants for the explorer window and resources.
/// </summary>
public static class ExplorerConstants
{
    /// <summary>
    /// File extension for web application file resources.
    /// </summary>
    public const string WebAppExtension = ".webapp";

    /// <summary>
    /// File extension for markdown file resources.
    /// </summary>
    public const string MarkdownExtension = ".md";

    /// <summary>
    /// File extension for python file resources.
    /// </summary>
    public const string PythonExtension = ".py";

    /// <summary>
    /// File extension for text file resources.
    /// </summary>
    public const string TextExtension = ".txt";

    /// <summary>
    /// File extension for Excel file resources.
    /// </summary>
    public const string ExcelExtension = ".xlsx";

    /// <summary>
    /// File extension for our virtual Application Settings resources.
    ///  NOTE : No real files of this type will ever be created. This is intended purely
    ///   to allow our custom Settings files editor to live within the document framework.
    /// </summary>
    public const string AppSettingsExtension = ".>appsettings";
}
