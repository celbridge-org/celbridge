namespace Celbridge.UserInterface;

/// <summary>
/// Defines the available application pages.
/// </summary>
public enum ApplicationPage
{
    /// <summary>
    /// No page is currently active.
    /// </summary>
    None,

    /// <summary>
    /// The home page showing recent projects and getting started content.
    /// </summary>
    Home,

    /// <summary>
    /// The workspace page for editing projects.
    /// </summary>
    Workspace,

    /// <summary>
    /// The settings page for configuring the application.
    /// </summary>
    Settings,

    /// <summary>
    /// The community page for accessing forums and community content.
    /// </summary>
    Community
}
